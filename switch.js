/// Now see the magic of switch

console.log('')

console.log('Now check out cucumber');

var cucumber = 'green';

switch (cucumber)

{
    case 'orange':
    console.log('This is a orange cucumber');
    break;

    
    case 'black':
    console.log('This is a black cucumber');
    break;

    case 'red':
    console.log('This is a red cucumber');
    break;

    case 'blue':
    console.log('This is a blue cucumber');
    break;

    case 'white':
    console.log('This is a white cucumber');
    break;

    case 'purple':
    console.log('This is a purple cucumber');
    break;

    case 'green':
    console.log('This is a green cucumber');
    break;

    default:
        console.log('There is no cucumber');

}


